class Article{
    // formatPath *** *** www.LazyCMS.net *** ***
    static function formatPath($p1,$p2,$p3,$p4=null){
        $p4 = empty($p4) ? now() : $p4;
        $R = str_replace(array('%I','%M','%P'),array($p1,md5($p1.salt(10)),pinyin($p3)),$p2);
        $R = strftime($R,$p4);
        return $R;
    }
}
