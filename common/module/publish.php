/**
 * 添加发布进程
 *
 * @param string $type
 * @param int $total
 * @param string $func
 * @param array $args
 * @return int
 */
function publish_add($type,$total,$func,$args=array()) {
    $db = get_conn();
    return $db->insert('#@_publish',array(
       'type'  => $type,
       'total' => $total,
       'func'  => $func,
       'args'  => serialize($args),
       'state' => 0,
    ));
}