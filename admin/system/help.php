// *** *** www.LazyCMS.net *** *** //
function lazy_main(){ 
    $path   = isset($_POST['path'])?$_POST['path']:null;
    if (strpos($path,'::')===false) {
        $module = isset($_POST['module'])?$_POST['module']:MODULE;
        $path   = $module.'::help/'.$path;
    }
    ajax_result(array(
        'TITLE' => t('help'),
        'BODY'  => ubbencode(t($path))
    ));
}
